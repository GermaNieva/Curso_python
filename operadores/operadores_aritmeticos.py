# operadores aritmeticos
suma = 10 + 5
resta = 10 - 5
multiplicacion = 10 * 5
division = 10 / 5 # division normal devuelve float
division_entera = 10 // 3  # division entera devuelve solo la parte entera
modulo = 10 % 3 # resto de la division devuelve el resto
exponente = 2 ** 3 # 2 elevado a la 3

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicacion:", multiplicacion)
print("Division:", division)
print("Division entera:", division_entera)
print("Modulo:", modulo)
print("Exponente:", exponente)

# operadores de asignacion
x = 10  
print("Valor inicial de x:", x)

tipo_de_dato=type(x) #type devuelve el tipo de dato de la variable
x += 5  # equivalente a x = x + 5
print("Tipo de dato de x:", tipo_de_dato)

