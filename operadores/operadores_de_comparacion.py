# operadores de comparacion
a = 10
es_igual = a==10  # igual a
es_distinto_de = a != 5  # distinto de
es_mayor_que = a > 5  # mayor que
es_menor_que = a < 15  # menor que
es_mayor_igual_que = a >= 10  # mayor o igual que
es_menor_igual_que = a <= 10  # menor o igual que

print("Es igual a 10:", es_igual)
print("Es distinto de 5:", es_distinto_de)
print("Es mayor que 5:", es_mayor_que)
print("Es menor que 15:", es_menor_que)
print("Es mayor o igual que 10:", es_mayor_igual_que)
print("Es menor o igual que 10:", es_menor_igual_que)

# calculos combinados
resultado = (a + 5) * 2 - 3 / 1.5
print("Resultado de la operación combinada:", resultado)
# operadores de comparacion
# Estos operadores se utilizan para comparar valores y devuelven un valor booleano (True o False).
# Los operadores de comparación son útiles para tomar decisiones en el código, como en estructuras condicionales o bucles.
# Por ejemplo, se pueden usar en una sentencia if para ejecutar un bloque de código solo si una condición es verdadera.
